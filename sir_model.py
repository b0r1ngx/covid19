def sir_model(params, t_max=100, Npop=1000, I_0=1):
    S = np.zeros(t_max)
    I = np.zeros(t_max)
    R = np.zeros(t_max)

    S[0], I[0], R[0] = Npop - I_0, I_0, 0

    beta, R_0 = params
